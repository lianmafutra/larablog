    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(TagRequest $request)
    {
        $tag =  Tag::updateOrCreate(
            ['id' => $request->id],
            ['name' => $request->name]
        );

        return response()->json($tag);
    }

